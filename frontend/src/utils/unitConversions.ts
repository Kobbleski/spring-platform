export function inchesToMm(inches: number): number {

    return inches * 25.4
}


export function mmToInches(mm: number): number {

    return mm / 25.4
}


export function lbfToN(lbf: number): number {

    return lbf * 4.44822
}


export function nToLbf(newtons: number): number {

    return newtons / 4.44822
}